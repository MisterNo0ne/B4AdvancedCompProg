
public class Sapient {
	//Attributes
	private int age;
	private String name;
	
	//Constructor
	public Sapient(int age, String name) {
		this.age = age;
		this.name = name;
	}
	
	//Getters
	public int getAge() {
		return age;
	}
	public String getName() {
		return name;
	}
	
	//Setters
	public void setAge(int age) {
		this.age = age;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
